#include "animal.h"

/****************************************************************************************
 Constructor of an Animal object.
****************************************************************************************/
Animal::Animal(int px, int py, int age, int energy, int gest, animal_t type)
{
    this->posx = px;
    this->posy = py;
    this->age = age;
    this->energy = energy;
    this->gestation = gest;
    this->type = type;
}

/****************************************************************************************
 Getter for an animal position.
****************************************************************************************/
void Animal::get_position(int &px, int &py) const
{
    px = posx;
    py = posy;
}

/****************************************************************************************
 Setter for an animal position.
****************************************************************************************/
void Animal::set_position(int px, int py)
{
    posx = px;
    posy = py;
}

/****************************************************************************************
 Getter for an animal energy.
****************************************************************************************/
int Animal::get_energy() const
{ 
    return energy; 
}

/****************************************************************************************
 Decrements the energy of an animal.
****************************************************************************************/
void Animal::dec_energy()
{ 
    --energy; 
}

/****************************************************************************************
 Resets the energy of an animal.
****************************************************************************************/
void Animal::kill()
{
    energy = 0;
}

/****************************************************************************************
 Getter for the animal type.
****************************************************************************************/
Animal::animal_t Animal::get_type() const
{
    return type;
}

