#include "shark.h"

/****************************************************************************************
 Constructor of Shark object.
****************************************************************************************/
Shark::Shark(int px, int py, int age): 
Animal(px, py, age, Shark::INIT_ENERGY, 0, Animal::TYPE_SHARK) {}

/****************************************************************************************
 Destructor of Shark object.
****************************************************************************************/
Shark::~Shark() {}

/****************************************************************************************
 Increments the age and gestation of a shark.
****************************************************************************************/
void Shark::inc_age()
{
    if (++age > Shark::NUM_DAYS_PUBERTY)
        gestation++;
}

/****************************************************************************************
 Indicates if the puberty of a shark has been reached.
****************************************************************************************/
bool Shark::puberty_reached() const
{
    return (age >= Shark::NUM_DAYS_PUBERTY && gestation > Shark::NUM_DAYS_GEST);
}

/****************************************************************************************
 Resets the gestation of a shark.
****************************************************************************************/
void Shark::reset_gestation()
{ 
    gestation = -Shark::NUM_DAYS_GEST; 
}

/****************************************************************************************
 Indicates the status of a shark.
****************************************************************************************/
bool Shark::is_dead() const
{
    return (age > Shark::MAX_AGE || energy <= 0);
}

/****************************************************************************************
 Increments the energy of a shark.
****************************************************************************************/
void Shark::inc_energy()
{
    energy += Shark::NUM_DAYS_DIGESTION;
}

